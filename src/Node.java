


public class Node {
	public int iData;              // data item (key)
	   public int height;             // height of node
	   public Node left;         // this node's left child
	   public Node right;        // this node's right child

	   public Node(int id) 			// constructor
	      {
	      iData = id;
	      height = 0;
	      left = null;
	      right = null;
	      }
}

 
